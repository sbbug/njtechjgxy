package com.njtechjgxy.vo;

public class PatentCheckResult {

	private int result_id;
	private int result_is_ok;
	private String result_rewards;
	private String result_reward_from_depart;
	private String result_message;
	private String patent_title;
	private String teacher_num;
	
	public int getResult_id() {
		return result_id;
	}
	public void setResult_id(int result_id) {
		this.result_id = result_id;
	}
	public int getResult_is_ok() {
		return result_is_ok;
	}
	public void setResult_is_ok(int result_is_ok) {
		this.result_is_ok = result_is_ok;
	}
	public String getResult_rewards() {
		return result_rewards;
	}
	public void setResult_rewards(String result_rewards) {
		this.result_rewards = result_rewards;
	}
	public String getResult_reward_from_depart() {
		return result_reward_from_depart;
	}
	public void setResult_reward_from_depart(String result_reward_from_depart) {
		this.result_reward_from_depart = result_reward_from_depart;
	}
	public String getResult_message() {
		return result_message;
	}
	public void setResult_message(String result_message) {
		this.result_message = result_message;
	}
	public String getPatent_title() {
		return patent_title;
	}
	public void setPatent_title(String patent_title) {
		this.patent_title = patent_title;
	}
	public String getTeacher_num() {
		return teacher_num;
	}
	public void setTeacher_num(String teacher_num) {
		this.teacher_num = teacher_num;
	}
	
}
