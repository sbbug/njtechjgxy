package com.njtechjgxy.vo;

public class TeacherLoginLog {

	private int teacher_id;
	private String teacher_num;
	private String teacher_login_time;
	
	public int getTeacher_id() {
		return teacher_id;
	}
	public void setTeacher_id(int teacher_id) {
		this.teacher_id = teacher_id;
	}
	public String getTeacher_num() {
		return teacher_num;
	}
	public void setTeacher_num(String teacher_num) {
		this.teacher_num = teacher_num;
	}
	public String getTeacher_login_time() {
		return teacher_login_time;
	}
	public void setTeacher_login_time(String teacher_login_time) {
		this.teacher_login_time = teacher_login_time;
	}
	
	
	
}
