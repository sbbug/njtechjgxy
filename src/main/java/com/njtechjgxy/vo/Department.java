package com.njtechjgxy.vo;

public class Department {

	private int depart_id;
	private String depart_item;
	private int depart_status;
	
	public int getDepart_id() {
		return depart_id;
	}
	public void setDepart_id(int depart_id) {
		this.depart_id = depart_id;
	}
	public String getDepart_item() {
		return depart_item;
	}
	public void setDepart_item(String depart_item) {
		this.depart_item = depart_item;
	}
	public int getDepart_status() {
		return depart_status;
	}
	public void setDepart_status(int depart_status) {
		this.depart_status = depart_status;
	}
	
	
}
