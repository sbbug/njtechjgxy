package com.njtechjgxy.vo;

public class SCD {

	private int scd_id;
	private String scd_name;
	private String scd_issn;
	private String scd_cn;
	private int scd_status;
	
	public int getScd_id() {
		return scd_id;
	}
	public void setScd_id(int scd_id) {
		this.scd_id = scd_id;
	}
	public String getScd_name() {
		return scd_name;
	}
	public void setScd_name(String scd_name) {
		this.scd_name = scd_name;
	}
	public String getScd_issn() {
		return scd_issn;
	}
	public void setScd_issn(String scd_issn) {
		this.scd_issn = scd_issn;
	}
	public String getScd_cn() {
		return scd_cn;
	}
	public void setScd_cn(String scd_cn) {
		this.scd_cn = scd_cn;
	}
	public int getScd_status() {
		return scd_status;
	}
	public void setScd_status(int scd_status) {
		this.scd_status = scd_status;
	}
	
}
